  CASE 
    WHEN gpa >= 3.8 THEN '优秀(3.8+)'
    WHEN gpa >= 3.5 THEN '良好(3.5-3.8)'
    WHEN gpa >= 3.0 THEN '中等(3.0-3.5)'
    ELSE '待提高(<3.0)'
  END as 绩点等级,
  COUNT(*) as 人数
FROM students 
GROUP BY 
  CASE 
    WHEN gpa >= 3.8 THEN '优秀(3.8+)'
    WHEN gpa >= 3.5 THEN '良好(3.5-3.8)'
    WHEN gpa >= 3.0 THEN '中等(3.0-3.5)'
    ELSE '待提高(<3.0)'
  END
ORDER BY MIN(gpa) DESC;

